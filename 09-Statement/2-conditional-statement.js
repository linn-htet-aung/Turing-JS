/**
 * ------------------------------------------------------------------------------------------
 * 1. if Statement
 * ------------------------------------------------------------------------------------------
 */
{
    if (true) {
        console.log("Hello World");
    }
}

/**
 * ------------------------------------------------------------------------------------------
 * 2. if else Statement
 * ------------------------------------------------------------------------------------------
 */
{
    if (true) {
        console.log("Hello World");
    } else {
        console.log("Hello Universe");
    }
}

/**
 * ------------------------------------------------------------------------------------------
 * 3. if elseif else Statement
 * ------------------------------------------------------------------------------------------
 */
{
    let x;
    if (x == 0) {
        console.log("Zero Condition");
    } else if (x == 1) {
        console.log("One Condition");
    } else {
        console.log("Other Condition");
    }
}

/**
 * ------------------------------------------------------------------------------------------
 * 4. switch Statement
 * ------------------------------------------------------------------------------------------
 */
{
    let x;
    switch (x) {
        case 1:
            console.log("One Condition");
            break;
        case 2:
            console.log("Two Condition");
            break;
        default:
            console.log("Default Condition");
            break;
    }
}
/**
 * ------------------------------------------------------------------------------------------
 * 1. label Statement
 * ------------------------------------------------------------------------------------------
 */
{
    mainloop: while(true) {
        console.log("Hello World");
        if(true) {
            break mainloop;
        }
    }
}

/**
 * ------------------------------------------------------------------------------------------
 * 2. break Statement
 * ------------------------------------------------------------------------------------------
 */
{
    for (let i = 0; i < 10; i++) {
        console.log("i:", i);
        if (i == 5) {
            break;
        }
    }
    console.log("End of Loop");
}

/**
 * ------------------------------------------------------------------------------------------
 * 3. continue Statement
 * ------------------------------------------------------------------------------------------
 */
{
    for(let i = 0; i < 10; i++) {
        if (i == 5) {
            continue;
        }
        console.log("i:", i);
    }
}

/**
 * ------------------------------------------------------------------------------------------
 * 4. return Statement
 * ------------------------------------------------------------------------------------------
 */
{
    function add(x, y) {
        console.log("x: ", x, " ", "y: ", y);
        return x + y;
        console.log("Never Run");
    }
    let result = add(10, 20);
    console.log("Result:", result);
}

/**
 * ------------------------------------------------------------------------------------------
 * 5. throw statement
 * ------------------------------------------------------------------------------------------
 */
{
    function div(x, y) {
        console.log("Div ", x , " ", y);
        if (isNaN(y)) {
            throw "Y is NaN";
        }
        return x / y;
    }
    let result = div(10, NaN);
    console.log("result:", result);
}

/**
 * ------------------------------------------------------------------------------------------
 * 6. try/catch/finally statement
 * ------------------------------------------------------------------------------------------
 */
{
    function div(x, y) {
        if (isNaN(y)) {
            throw {
                message: "Y is NaN",
            };
        }
        return x / y;
    }
    
    try {
        let result = div(10, NaN);
        console.log("result:", result);
    }
    catch(e) {
        console.log("Error:", e.message);
    }
    finally {
        console.log("Finally Ever Run & Always Win");
    }
}
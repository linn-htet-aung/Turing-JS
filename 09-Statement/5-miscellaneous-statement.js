/**
 * ------------------------------------------------------------------------------------------
 * 1. with Statement
 * ------------------------------------------------------------------------------------------
 */
{
    let obj = {
        name: "Alice",
        age: 23,
    };
    with(obj) {
        name = "Changed",
        age = 33,
        console.log("Hello World");
    }
    console.log("Obj:", obj);
}

/**
 * ------------------------------------------------------------------------------------------
 * 2. debugger Statement
 * ------------------------------------------------------------------------------------------
 */
 {
    // debugger; // start debugging here
    let obj = {
        name: "Alice",
        age: 23,
    };
    with(obj) {
        name = "Changed",
        age = 33,
        console.log("Hello World");
    }
    console.log("Obj:", obj);
}


/**
 * ------------------------------------------------------------------------------------------
 * 1. Single Assignment
 * - Right to Left Working
 * ------------------------------------------------------------------------------------------
 */
{
    let a = 10;
    let name = "Alice";
    let c = b = 3;
    console.log("a:", a);
    console.log("name:", name);
    console.log("c, b:", c, b);
}

/**
 * ------------------------------------------------------------------------------------------
 * 2. Compound Assignment
 * - Right to Left Working
 * ------------------------------------------------------------------------------------------
 */
{
    let a = 10;
    let b = 20;

    console.log(a += b + 2); // a = (a + (b + 2))
}
